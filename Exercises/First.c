#include<stdio.h>

int main()
{
    int i = 20;                
    float f = 0.2;             
    char c = 'v';              
    char arr[15] = "example";  

    printf("Integer value: %d\n", i);          
    printf("Floating-point value: %.2f\n", f); 
    printf("Character value: %c\n", c);       
    printf("String value: %s\n", arr);        

    return 0; 
}
